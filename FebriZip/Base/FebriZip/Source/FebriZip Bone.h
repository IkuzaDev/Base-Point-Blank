extern D3DVIEWPORT9 Viewport;
bool WorldToScreen2(D3DVECTOR Player, D3DVECTOR &PlayerScaled, LPDIRECT3DDEVICE9 pDevice)
{

	D3DXVECTOR3 PlayerPos(Player.x, Player.y, Player.z);
	D3DXVECTOR3 vScreen;
	pDevice->GetViewport(&Viewport);
	Viewport.X = Viewport.Y = 0;
	Viewport.MinZ = 0;
	Viewport.MaxZ = 1;
	D3DXVec3Project(&vScreen, &PlayerPos, &Viewport,
		i3RenderContext2::Singleton()->GetProjectMatrix(),
		i3RenderContext2::Singleton()->GetViewMatrix(),
		i3RenderContext2::Singleton()->GetWorldMatrix());

	if (vScreen.z < 1.0f && vScreen.x > 0.0f && vScreen.y > 0.0f && vScreen.x < Viewport.Width && vScreen.y < Viewport.Height)
	{
		PlayerScaled.x = vScreen.x;
		PlayerScaled.y = vScreen.y;
		PlayerScaled.z = vScreen.z;
		return true;
	}
	return false;
}


